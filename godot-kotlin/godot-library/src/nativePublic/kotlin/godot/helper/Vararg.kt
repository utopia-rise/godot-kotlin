package godot.helper

import godot.core.Variant

inline fun <reified P0> arrayOfVariant(
    p0: P0
): Array<Variant> = arrayOf(
    Variant.wrap(p0)
)

inline fun <reified P0, reified P1> arrayOfVariant(
    p0: P0,
    p1: P1
): Array<Variant> = arrayOf(
    Variant.wrap(p0),
    Variant.wrap(p1)
)

