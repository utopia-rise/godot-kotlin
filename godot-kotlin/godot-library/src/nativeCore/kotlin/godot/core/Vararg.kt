package godot.core

inline fun <reified P0> arrayOfVariant(
    p0: P0
): Array<Variant> = arrayOf(
    p0.toVariant()
)

inline fun <reified P0, reified P1> arrayOfVariant(
    p0: P0,
    p1: P1
): Array<Variant> = arrayOf(
    p0.toVariant(),
    p1.toVariant()
)

